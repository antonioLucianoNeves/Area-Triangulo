using System;
using System.Collections.Generic;
using System.Text;

namespace AreaRetangulo
{
    class Retangulo
    {
        public int altura;
        public int largura;

        public int Area() {
            return altura * largura;

            
        }
        public int Perimetro()
        {
            return altura * largura;

        }
        public int Diagonal()
        {
            return altura * largura;
        }
    }
    
    

}
